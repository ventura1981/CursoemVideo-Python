# Crie um programa que leia dois números e mostre a soma entre eles

n1 = float(input('Entre com o primeiro valor numérico:'))

n2 = float(input('Entre com o segundo valor numérico:'))

soma = n1 + n2

print('O resultado da soma entre',n1,'e',n2,'é',soma)
print('O resultado da soma entre {} e {} é {}'.format(n1, n2, soma))